const propTypesTemplate = ({ imports, interfaces, componentName, jsx, exports }, { tpl }) => {
  return tpl`${imports}
import ISvgProps from "../types";
${interfaces}

function ${componentName}(props: ISvgProps) {
  const sizeCheck = (value: any) => {
    if (props.size === "small") {
      return 20;
    } else if (props.size === "regular") {
      return 24;
    } else if (props.size === "large") {
      return 36;
    } else {
      return value;
    }
  };
  const colorCheck = (value: any) => {
    if (value === "primary") {
      return "#0088FF";
    } else if (value === "success") {
      return "#0FD186";
    } else if (value === "error") {
      return "#FF4D4D";
    } else if (value === "warning") {
      return "#FFAE06";
    } else {
      return value;
    }
  };
  return ${jsx};
}

${exports}
  `;
};

module.exports = propTypesTemplate;
